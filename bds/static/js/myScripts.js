// For RAJUK //
function rajukFunction() {
  var checkBox = document.getElementById("rajukCk");
  var browse = document.getElementById("rajuk");

  if (checkBox.checked == true){
    browse.style.opacity = 1;
    browse.style.filter = alpha(opacity=100);
  }
  else {
      browse.style.opacity = 0.3;
      browse.style.filter = alpha(opacity=30);
  }
}

